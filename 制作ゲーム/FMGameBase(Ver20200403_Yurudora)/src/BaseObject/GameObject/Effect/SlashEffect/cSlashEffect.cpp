#include "cSlashEffect.h"
#include "..\cEffectManager.h"

//==========================================================================================
// 定数
//==========================================================================================
const int cSlashEffect::PRIORITY = 400;
const float cSlashEffect::SCALE = 25.0f;

//==========================================================================================
// コンストラクタ
//==========================================================================================
cSlashEffect::cSlashEffect( IBaseObject* parent )
	: cEffectObject( parent, "SlashEffect", cEffectManager::GetInstance().GetEffectFilename( EFFECT_ID::SLASH ) )
{
}

//==========================================================================================
// デストラクタ
//==========================================================================================
cSlashEffect::~cSlashEffect( void )
{
}

//==========================================================================================
// 初期化
//==========================================================================================
void cSlashEffect::Initialize( void )
{

	SetPriority( PRIORITY );
	SetScale( SCALE );

	cEffectObject::Initialize();
}