#include "cFlameEffect.h"
#include "..\cEffectManager.h"

//==========================================================================================
// 定数
//==========================================================================================
const int cFlameEffect::PRIORITY = 300;
const float cFlameEffect::SCALE = 100.0f;

//==========================================================================================
// コンストラクタ
//==========================================================================================
cFlameEffect::cFlameEffect(IBaseObject* parent)
	: cEffectObject(parent, "Flame", cEffectManager::GetInstance().GetEffectFilename(EFFECT_ID::FLAME))
{
}

//==========================================================================================
// デストラクタ
//==========================================================================================
cFlameEffect::~cFlameEffect(void)
{
}

//==========================================================================================
// 初期化
//==========================================================================================
void cFlameEffect::Initialize(void)
{

	SetPriority(PRIORITY);
	SetScale(SCALE);

	cEffectObject::Initialize();
}