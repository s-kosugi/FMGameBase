#include "cVector3.h"

//==========================================================================================
//  コンストラクタ
//==========================================================================================
cVector3::cVector3(void) :
	x(0.0f),y(0.0f),z(0.0f)
{
}
//==========================================================================================
//  コンストラクタ
//  x : x成分
//  y : y成分
//==========================================================================================
cVector3::cVector3(float x, float y) :
	x(x),y(y),z(0.0f)
{
}

//==========================================================================================
//  コンストラクタ
//  x : x成分
//  y : y成分
//  z : z成分
//==========================================================================================
cVector3::cVector3(float x, float y, float z) :
	x( x ), y ( y ) , z ( z )
{
}
//==========================================================================================
//  コンストラクタ
//  v : ベクトル
//==========================================================================================
cVector3::cVector3(const cVector3 & v) :
	x(v.x),y(v.y),z(v.z)
{
}
//==========================================================================================
//  デストラクタ
//==========================================================================================
cVector3::~cVector3()
{
}
