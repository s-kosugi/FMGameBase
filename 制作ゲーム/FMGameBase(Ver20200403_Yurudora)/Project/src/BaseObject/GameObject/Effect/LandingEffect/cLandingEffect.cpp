#include "cLandingEffect.h"
#include "..\cEffectManager.h"

//==========================================================================================
// 定数
//==========================================================================================
const int cLandingEffect::PRIORITY = 199;
const float cLandingEffect::SCALE = 35.0f;

//==========================================================================================
// コンストラクタ
//==========================================================================================
cLandingEffect::cLandingEffect(IBaseObject* parent)
	: cEffectObject(parent, "LandingEffect", cEffectManager::GetInstance().GetEffectFilename(EFFECT_ID::LANDING))
{
}

//==========================================================================================
// デストラクタ
//==========================================================================================
cLandingEffect::~cLandingEffect(void)
{
}

//==========================================================================================
// 初期化
//==========================================================================================
void cLandingEffect::Initialize(void)
{

	SetPriority(PRIORITY);
	SetScale(SCALE);

	cEffectObject::Initialize();
}