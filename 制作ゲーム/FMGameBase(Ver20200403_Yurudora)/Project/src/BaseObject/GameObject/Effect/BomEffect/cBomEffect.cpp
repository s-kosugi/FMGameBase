#include "cBomEffect.h"
#include "..\cEffectManager.h"

//==========================================================================================
// 定数
//==========================================================================================
const int cBomEffect::PRIORITY = 300;
const float cBomEffect::SCALE = 1.0f;

//==========================================================================================
// コンストラクタ
//==========================================================================================
cBomEffect::cBomEffect(IBaseObject * parent)
	: cEffectObject(parent, "BomEffect", cEffectManager::GetInstance().GetEffectFilename(EFFECT_ID::BOM))
{
}

//==========================================================================================
// デストラクタ
//==========================================================================================
cBomEffect::~cBomEffect(void)
{
}

//==========================================================================================
// 初期化
//==========================================================================================
void cBomEffect::Initialize(void)
{

	SetPriority(PRIORITY);
	SetScale(SCALE);

	cEffectObject::Initialize();
}