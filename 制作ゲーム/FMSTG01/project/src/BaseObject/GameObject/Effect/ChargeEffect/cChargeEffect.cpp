#include "cChargeEffect.h"

//==========================================================================================
// コンストラクタ
//==========================================================================================
cChargeEffect::cChargeEffect(IBaseObject * parent)
	: cEffectObject(parent, "Hit", "data\\effect\\charge.efk")
{
}

//==========================================================================================
// デストラクタ
//==========================================================================================
cChargeEffect::~cChargeEffect(void)
{
}

//==========================================================================================
// 初期化
//==========================================================================================
void cChargeEffect::Initialize(void)
{

	SetPriority(PRIORITY);

	cEffectObject::Initialize();
}

//==========================================================================================
// 更新
//==========================================================================================
void cChargeEffect::Update(void)
{
	cEffectObject::Update();
}

//==========================================================================================
// 破棄
//==========================================================================================
IBaseObject* cChargeEffect::Finalize(void)
{
	cEffectObject::Finalize();

	return this;
}