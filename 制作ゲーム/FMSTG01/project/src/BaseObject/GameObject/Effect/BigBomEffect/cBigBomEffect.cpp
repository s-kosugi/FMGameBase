#include "cBigBomEffect.h"
#include "..\..\..\..\SoundCtrl\cSoundCtrl.h"

//==========================================================================================
// コンストラクタ
//==========================================================================================
cBigBomEffect::cBigBomEffect(IBaseObject * parent)
	: cEffectObject(parent, "BigBom", "data\\effect\\bigbom.efk")
{
}

//==========================================================================================
// デストラクタ
//==========================================================================================
cBigBomEffect::~cBigBomEffect(void)
{
}

//==========================================================================================
// 初期化
//==========================================================================================
void cBigBomEffect::Initialize(void)
{
	cSoundCtrl::GetInstance().Play(SOUND_BOM);
	SetPriority(PRIORITY);

	cEffectObject::Initialize();
}

//==========================================================================================
// 更新
//==========================================================================================
void cBigBomEffect::Update(void)
{
	cEffectObject::Update();
}

//==========================================================================================
// 破棄
//==========================================================================================
IBaseObject* cBigBomEffect::Finalize(void)
{
	cEffectObject::Finalize();

	return this;
}