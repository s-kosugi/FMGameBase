{
}

//==========================================================================================
// デストラクタ
//==========================================================================================
cHitEffect::~cHitEffect(void)
{
}

//==========================================================================================
// 初期化
//==========================================================================================
void cHitEffect::Initialize(void)
{

	SetPriority(PRIORITY);

	cEffectObject::Initialize();
}

//==========================================================================================
// 更新
//==========================================================================================
void cHitEffect::Update(void)
{
	cEffectObject::Update();
}

//==========================================================================================
// 破棄
//==========================================================================================
IBaseObject* cHitEffect::Finalize(void)
{
	cEffectObject::Finalize();

	return this;
}