#include "cSmokeEffect.h"

//==========================================================================================
// コンストラクタ
//==========================================================================================
cSmokeEffect::cSmokeEffect(IBaseObject * parent)
	: cEffectObject(parent, "Smoke", "data\\effect\\smoke.efk"),
	m_pFollowingObj(nullptr)
{
}

//==========================================================================================
// デストラクタ
//==========================================================================================
cSmokeEffect::~cSmokeEffect(void)
{
}

//==========================================================================================
// 初期化
//==========================================================================================
void cSmokeEffect::Initialize(void)
{

	SetPriority(PRIORITY);

	cEffectObject::Initialize();
}

//==========================================================================================
// 更新
//==========================================================================================
void cSmokeEffect::Update(void)
{
	// 追従オブジェクトがあれば位置を追従させる
	if(m_pFollowingObj != nullptr) m_vPos = m_pFollowingObj->GetPos();

	cEffectObject::Update();
}

//==========================================================================================
// 破棄
//==========================================================================================
IBaseObject* cSmokeEffect::Finalize(void)
{
	cEffectObject::Finalize();

	return this;
}