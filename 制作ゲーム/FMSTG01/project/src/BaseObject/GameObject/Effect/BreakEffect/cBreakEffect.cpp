#include "cBreakEffect.h"
#include "..\..\..\..\SoundCtrl\cSoundCtrl.h"

//==========================================================================================
// コンストラクタ
//==========================================================================================
cBreakEffect::cBreakEffect(IBaseObject * parent)
	: cEffectObject(parent, "Bom", "data\\effect\\break.efk")
{
}

//==========================================================================================
// デストラクタ
//==========================================================================================
cBreakEffect::~cBreakEffect(void)
{
}

//==========================================================================================
// 初期化
//==========================================================================================
void cBreakEffect::Initialize(void)
{
	cSoundCtrl::GetInstance().Play(SOUND_BOM);
	SetPriority(PRIORITY);

	cEffectObject::Initialize();
}

//==========================================================================================
// 更新
//==========================================================================================
void cBreakEffect::Update(void)
{
	cEffectObject::Update();
}

//==========================================================================================
// 破棄
//==========================================================================================
IBaseObject* cBreakEffect::Finalize(void)
{
	cEffectObject::Finalize();

	return this;
}