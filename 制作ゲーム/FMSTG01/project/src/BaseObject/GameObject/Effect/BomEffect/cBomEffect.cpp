#include "cBomEffect.h"
#include "..\..\..\..\SoundCtrl\cSoundCtrl.h"

//==========================================================================================
// コンストラクタ
//==========================================================================================
cBomEffect::cBomEffect(IBaseObject * parent)
	: cEffectObject(parent, "Bom", "data\\effect\\bom.efk")
{
}

//==========================================================================================
// デストラクタ
//==========================================================================================
cBomEffect::~cBomEffect(void)
{
}

//==========================================================================================
// 初期化
//==========================================================================================
void cBomEffect::Initialize(void)
{
	cSoundCtrl::GetInstance().Play(SOUND_BOM);
	SetPriority(PRIORITY);

	cEffectObject::Initialize();
}

//==========================================================================================
// 更新
//==========================================================================================
void cBomEffect::Update(void)
{
	cEffectObject::Update();
}

//==========================================================================================
// 破棄
//==========================================================================================
IBaseObject* cBomEffect::Finalize(void)
{
	cEffectObject::Finalize();

	return this;
}