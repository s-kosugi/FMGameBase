#include "IBaseScene.h"
#include "..\cSceneManager.h"

//==========================================================================================
//  コンストラクタ
//==========================================================================================
IBaseScene::IBaseScene(IBaseObject* parent,const std::string& name)
	: IBaseObject(parent, name),
	m_eSceneID(SCENE_ID::NONE)
{
}

//==========================================================================================
//  コンストラクタ
//==========================================================================================
IBaseScene::~IBaseScene( void )
{
}

//==========================================================================================
//  シーン切り替え
//==========================================================================================
void IBaseScene::Change(SCENE_ID id)
{
	// シーンを変更する
	cSceneManager::GetInstance().ChangeScene(id);
}

//==========================================================================================
//  シーンのスタック
//==========================================================================================
void IBaseScene::Push(SCENE_ID id)
{
	// シーンをスタックする
	cSceneManager::GetInstance().PushScene(id);

	// 自分は更新を停止し、描画のみ行う
	m_eObjectState = OBJECT_STATE::WAIT;
}

//==========================================================================================
//  スタックされたシーンの削除
//==========================================================================================
void IBaseScene::Pop(void)
{
	// スタックされたシーンを取り出す
	cSceneManager::GetInstance().PopScene();
}
//==========================================================================================
// シーンIDの設定
//==========================================================================================
void IBaseScene::SetSceneID(SCENE_ID id)
{
	m_eSceneID = id;
}
