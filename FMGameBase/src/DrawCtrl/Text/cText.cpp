#include "cText.h"
#include "DrawCtrl/cDrawCtrl.h"

//==========================================================================================
//  コンストラクタ
//==========================================================================================
cText::cText(const std::string& text) :
	IDrawBase(""),
	m_sText(text),
	m_nEdgeColor(m_nColor),
	m_nVerticalFlag(FALSE)
{
	cDrawCtrl::GetInstance().RegistDrawObject(*this);
}
//==========================================================================================
//  コンストラクタ
//==========================================================================================
cText::cText(const std::string& text, const std::string& fontname, int fontsize, int Thick, int FontType, int EdgeSize, int Italic) :
	IDrawBase(""),
	m_sText(text),
	m_nEdgeColor(m_nColor),
	m_nVerticalFlag(FALSE)
{
	cDrawCtrl::GetInstance().RegistDrawObject(*this, fontname, fontsize, Thick, FontType, EdgeSize, Italic);
}
//==========================================================================================
//  デストラクタ
//==========================================================================================
cText::~cText(void)
{
}

//==========================================================================================
//  初期化
//==========================================================================================
void
cText::
Initialize(void)
{
	IDrawBase::Initialize();
}

//==========================================================================================
//  更新
//==========================================================================================
void cText::Update(void)
{
	IDrawBase::Update();
}
//==========================================================================================
//  解放
//==========================================================================================
void cText::Finalize(void)
{
	IDrawBase::Finalize();
}
//==========================================================================================
//  文字列の幅を取得する
//  指定のフォントデータで描画する文字列の幅(ドット単位)を得る
//==========================================================================================
int cText::GetWidth(void)
{
	return cDrawCtrl::GetInstance().GetTextWidth(this);
}
