#include "..\cDrawCtrl.h"
#include "cModel.h"

//==========================================================================================
//  コンストラクタ
//==========================================================================================
cModel::cModel(const std::string& filename) :
	m_vAngle(0.0f, 0.0f, 0.0f),
	m_vScale(1.0f, 1.0f, 1.0f),
	IDrawBase(filename)
{
	// 描画情報を登録
	cDrawCtrl::GetInstance().RegistDrawObject(*this, filename);
}

//==========================================================================================
//  デストラクタ
//==========================================================================================
cModel::~cModel(void)
{
}

//==========================================================================================
//  初期化
//==========================================================================================
void
cModel::
Initialize(void)
{
	IDrawBase::Initialize();
}

//==========================================================================================
//  更新
//==========================================================================================
void cModel::Update(void)
{
	IDrawBase::Update();
}

//==========================================================================================
//  解放
//==========================================================================================
void cModel::Finalize(void)
{
	IDrawBase::Finalize();
}