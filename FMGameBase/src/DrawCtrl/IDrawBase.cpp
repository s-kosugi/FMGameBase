#include "IDrawBase.h"
#include "..\DrawCtrl\cDrawCtrl.h"

//==========================================================================================
//  コンストラクタ
//==========================================================================================
IDrawBase::IDrawBase() :
	m_vPos( 0.0f,0.0f),
	m_nPri(0),
	m_nColor(COLOR_DEFAULT),
	m_bVisible(true),
	m_bUnload(false),
	m_nGraphHandle(LOADGRAPH_NONE),
	m_sFileName(""),
	m_eDrawType(DRAWTYPE::SPRITE)
{
}

//==========================================================================================
//  コンストラクタ
//==========================================================================================
IDrawBase::IDrawBase(const std::string& filename) :
	m_vPos( 0.0f,0.0f ),
	m_nPri(0),
	m_nColor(COLOR_DEFAULT),
	m_bVisible(true),
	m_bUnload(false),
	m_nGraphHandle(LOADGRAPH_NONE),
	m_sFileName(filename),
	m_eDrawType(DRAWTYPE::SPRITE)
{
}

//==========================================================================================
//  デストラクタ
//==========================================================================================
IDrawBase::~IDrawBase(void)
{
	cDrawCtrl::GetInstance().RemoveDrawObject(this);
}

//==========================================================================================
//  初期化
//==========================================================================================
void
IDrawBase::
Initialize(void)
{
}

//==========================================================================================
//  更新
//==========================================================================================
void IDrawBase::Update(void)
{
}

//==========================================================================================
//  解放
//==========================================================================================
void IDrawBase::Finalize(void)
{
}
//==========================================================================================
// 描画表示優先度の変更
//==========================================================================================
void IDrawBase::SetPriority(int pri)
{
	if (m_nPri == pri) return;
	m_nPri = pri;
	// 描画の表示優先度変更要求
	cDrawCtrl::GetInstance().RequestChangeDrawPriority();
}