//==========================================================================================
cAnimSpObject::~cAnimSpObject(void)
{
}

//==========================================================================================
//  初期化
//==========================================================================================
void
cAnimSpObject::
Initialize(void)
{
	IBaseObject::Initialize();
	cAnimSprite::Initialize();
}

//==========================================================================================
//  初期化
//==========================================================================================
void cAnimSpObject::Initialize( const cVector2& pos )
{
	m_vPos = pos;
	Initialize();
}

//==========================================================================================
//  更新
//==========================================================================================
void cAnimSpObject::Update(void)
{
	IBaseObject::Update();
	cAnimSprite::Update();
}

//==========================================================================================
//  解放
//==========================================================================================
IBaseObject* cAnimSpObject::Finalize(void)
{
	IBaseObject::Finalize();
	cAnimSprite::Finalize();
	return this;
}