#include "cChargeEffect.h"
#include "..\cEffectManager.h"

//==========================================================================================
// 定数
//==========================================================================================
const int cChargeEffect::PRIORITY = 300;
const float cChargeEffect::SCALE = 1;

//==========================================================================================
// コンストラクタ
//==========================================================================================
cChargeEffect::cChargeEffect(IBaseObject * parent)
	: cEffectObject(parent, "ChargeEffect", cEffectManager::GetInstance().GetEffectFilename(EFFECT_ID::CHARGE))
{
}

//==========================================================================================
// デストラクタ
//==========================================================================================
cChargeEffect::~cChargeEffect(void)
{
}

//==========================================================================================
// 初期化
//==========================================================================================
void cChargeEffect::Initialize(void)
{

	SetPriority(PRIORITY);
	SetScale(SCALE);

	cEffectObject::Initialize();
}